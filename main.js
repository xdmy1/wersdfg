function openSidebar() {
    const sidebar = document.querySelector('.mobile-ul')
    sidebar.style.display = 'flex'
    const sidebar1 = document.querySelector('.burger')
    sidebar1.style.display = 'none'
    const sidebar2 = document.querySelector('.close')
    sidebar2.style.display = 'flex'
}

function closeSidebar() {
    const sidebar3 = document.querySelector('.mobile-ul')
    sidebar3.style.display = 'none'
    const sidebar4 = document.querySelector('.burger')
    sidebar4.style.display = 'flex'
    const sidebar5 = document.querySelector('.close')
    sidebar5.style.display = 'none'

}
